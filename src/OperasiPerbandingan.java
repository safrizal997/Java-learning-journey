public class OperasiPerbandingan {
    public static void main(String[] args) {

        // Valuenya hanya true/benar dan false/salah
        int a = 100;
        int b = 100;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b); // tidak sama dengan


    }
}

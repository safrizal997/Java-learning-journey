public class TipeDataKarakter {
    public static void main(String[] args) {

        // Tipe data karakter/huruf dipresentasikan oleh tipe (char) menggunakan tanda petik satu(') //

        char r = 'R';
        char i = 'I';
        char z = 'Z';
        char a = 'A';
        char l = 'L';

        System.out.print(r);
        System.out.print(i);
        System.out.print(z);
        System.out.print(a);
        System.out.println(l);
        System.out.println(i);
        System.out.println(z);
        System.out.println(a);
        System.out.println(l);
    }
}

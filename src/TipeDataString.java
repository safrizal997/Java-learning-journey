public class TipeDataString {
    public static void main(String[] args) {

        /* -Berisi kumpulan karakter atau teks
           -direpresentasikan dengan kata kunci string
           -menggunakan " (petik dua) sebelum dan sesudah teks
           -Default value dari string adalah null (tidak ada data)
        */

        String firstname = "Rizal";
        String lastname = "Gober";

        System.out.println(firstname);
        System.out.println(lastname);

        // Menggabungkan String //
        String fullname = firstname + " " + lastname;
        System.out.println(fullname);



    }
}
